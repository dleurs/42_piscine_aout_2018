#include <stdlib.h>

int		*ft_range(int min, int max)
{
	int i;
	int *tab;

	if (min < -2147483648 || max > 2147483647 || min >= max)
		return (NULL);
	tab = (int*)malloc(sizeof(int) * (max - min));
	if (tab == NULL)
		return (NULL);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		i++;
		min++;
	}
	return (tab);
}
